package sound;

/**
 * An Enum representing a Key Signature 
 * @author jains
 *
 */
public enum KeySignature {
	// MAJOR KEYS
	// Sharp Keys
	C, G, D, A, E, B, Fs,
	// Flat Keys
	F, Bb, Eb, Ab, Db, Gb,
	
	// MINOR KEYS
	// Sharp Keys
	Am, Em, Bm, Fsm, Csm, Gsm, Dsm,
	// Flat Keys
	Fm, Bbm, Ebm, Abm, Dbm, Gbm
	
}
